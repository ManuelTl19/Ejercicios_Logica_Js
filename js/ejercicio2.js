/*
Operaciones matemáticas: Crea un programa que sume, reste, multiplique y
divida dos números y muestre el resultado en la consola.
 */

let numero1=parseFloat(prompt("Inserta un número: "));
let numero2=parseFloat(prompt("Inserta otro número: "));

console.log("Los numeros son ", numero1, " y ", numero2);

console.log("La suma es: ", numero1 + numero2);
console.log("Su residuo es: ", numero1 - numero2);
console.log("Su productos es: ", numero1 * numero2);
console.log("Su division da: ", numero1 / numero2);